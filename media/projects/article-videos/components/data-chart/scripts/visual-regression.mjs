#!/usr/bin/env node
// Visual regression harness for this HyperFrames component. It compares the
// freshly generated snapshots/ directory to committed baselines/ and exits
// non-zero on missing, stale, resized, or meaningfully changed images.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import pixelmatch from 'pixelmatch';
import { PNG } from 'pngjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const COMPONENT_DIR = path.resolve(__dirname, '..');
const SNAPSHOTS_DIR = path.join(COMPONENT_DIR, 'snapshots');
const BASELINES_DIR = path.join(COMPONENT_DIR, 'baselines');
const DIFFS_DIR = path.join(COMPONENT_DIR, 'diffs');

const THRESHOLD = Number(process.env.VREG_THRESHOLD || 0.05);
const FAIL_PCT = Number(process.env.VREG_FAIL_PCT || 0.1);
const UPDATE = process.env.VREG_UPDATE === '1';

fs.mkdirSync(BASELINES_DIR, { recursive: true });
fs.mkdirSync(DIFFS_DIR, { recursive: true });

function normalizeSnapshot(name, srcPath) {
  // Strip non-deterministic metadata from PNG so pixel diffs are stable.
  const buf = fs.readFileSync(srcPath);
  const png = PNG.sync.read(buf);
  return { name, png, width: png.width, height: png.height };
}

function existingBaseline(name) {
  const p = path.join(BASELINES_DIR, name);
  if (!fs.existsSync(p)) return null;
  const buf = fs.readFileSync(p);
  return PNG.sync.read(buf);
}

function ensureDirFor(p) {
  fs.mkdirSync(path.dirname(p), { recursive: true });
}

function compare(current, baseline, name) {
  if (current.width !== baseline.width || current.height !== baseline.height) {
    return {
      name,
      pass: false,
      reason: `size changed (${baseline.width}×${baseline.height} → ${current.width}×${current.height})`,
      diffPct: 100,
    };
  }

  const { width, height } = current;
  const diff = new PNG({ width, height });
  const diffPx = pixelmatch(
    current.png.data,
    baseline.data,
    diff.data,
    width,
    height,
    { threshold: THRESHOLD, alpha: 0.1 },
  );
  const total = width * height;
  const diffPct = (diffPx / total) * 100;

  const diffPath = path.join(DIFFS_DIR, `diff-${name}`);
  if (diffPct >= FAIL_PCT) {
    ensureDirFor(diffPath);
    fs.writeFileSync(diffPath, PNG.sync.write(diff));
  }

  return {
    name,
    pass: diffPct < FAIL_PCT,
    diffPct,
    diffPath: diffPct >= FAIL_PCT ? diffPath : undefined,
  };
}

async function captureBaselineFrames() {
  // Re-use deterministic hyperframes snapshot output captured by `npm run snapshot`.
  const frames = fs.readdirSync(SNAPSHOTS_DIR)
    .filter((f) => f.endsWith('.png'))
    .sort();
  const baselineFrames = fs.existsSync(BASELINES_DIR)
    ? fs.readdirSync(BASELINES_DIR).filter((f) => f.endsWith('.png')).sort()
    : [];

  if (!frames.length) {
    console.error('No snapshots found. Run `npm run snapshot` first.');
    process.exit(1);
  }

  const results = [];
  for (const frame of frames) {
    const current = normalizeSnapshot(frame, path.join(SNAPSHOTS_DIR, frame));
    if (UPDATE) {
      ensureDirFor(path.join(BASELINES_DIR, frame));
      fs.copyFileSync(path.join(SNAPSHOTS_DIR, frame), path.join(BASELINES_DIR, frame));
      results.push({ name: frame, pass: true, updated: true, diffPct: 0 });
    } else {
      const baseline = existingBaseline(frame);
      if (!baseline) {
        results.push({ name: frame, pass: false, reason: 'missing baseline', diffPct: 100, missing: true });
      } else {
        results.push(compare(current, baseline, frame));
      }
    }
  }
  for (const stale of baselineFrames.filter((frame) => !frames.includes(frame))) {
    if (UPDATE) {
      fs.rmSync(path.join(BASELINES_DIR, stale));
    } else {
      results.push({
        name: stale,
        pass: false,
        reason: 'stale baseline (no matching current snapshot)',
        diffPct: 100,
      });
    }
  }
  return results;
}

async function captureContactSheet() {
  // The contact-sheet is generated by hyperframes snapshot. We compare it to a
  // committed baseline as a second-level regression guard.
  const name = 'contact-sheet.jpg';
  const currentPath = path.join(SNAPSHOTS_DIR, name);
  const baselinePath = path.join(BASELINES_DIR, name);
  if (!fs.existsSync(currentPath)) {
    return { name, pass: false, reason: 'missing contact sheet' };
  }
  if (UPDATE) {
    fs.copyFileSync(currentPath, baselinePath);
    return { name, pass: true, updated: true };
  }
  if (!fs.existsSync(baselinePath)) {
    return { name, pass: false, reason: 'missing baseline', missing: true };
  }
  // JPEG pixel comparison: decode both with sharp, compare via pixelmatch.
  const sharp = (await import('sharp')).default;
  const [cur, base] = await Promise.all([
    sharp(currentPath).raw().ensureAlpha().toBuffer({ resolveWithObject: true }),
    sharp(baselinePath).raw().ensureAlpha().toBuffer({ resolveWithObject: true }),
  ]);
  if (cur.info.width !== base.info.width || cur.info.height !== base.info.height) {
    return { name, pass: false, reason: `size changed (${base.info.width}×${base.info.height} → ${cur.info.width}×${cur.info.height})`, diffPct: 100 };
  }
  const diff = new PNG({ width: cur.info.width, height: cur.info.height });
  const diffPx = pixelmatch(cur.data, base.data, diff.data, cur.info.width, cur.info.height, { threshold: THRESHOLD });
  const diffPct = (diffPx / (cur.info.width * cur.info.height)) * 100;
  if (diffPct >= FAIL_PCT) {
    const diffPath = path.join(DIFFS_DIR, 'diff-contact-sheet.png');
    fs.writeFileSync(diffPath, PNG.sync.write(diff));
  }
  return { name, pass: diffPct < FAIL_PCT, diffPct, diffPath: diffPct >= FAIL_PCT ? path.join(DIFFS_DIR, 'diff-contact-sheet.png') : undefined };
}

const frameResults = await captureBaselineFrames();
const contactResult = await captureContactSheet();
const results = [...frameResults, contactResult];

let passed = 0;
let failed = 0;
let missing = 0;
for (const r of results) {
  if (r.pass) {
    passed++;
    console.log(`✓ ${r.name}${r.updated ? ' (updated baseline)' : r.diffPct !== undefined ? ` (${r.diffPct.toFixed(3)}% diff)` : ''}`);
  } else if (r.missing) {
    missing++;
    failed++;
    console.error(`✗ ${r.name}: ${r.reason}`);
  } else {
    failed++;
    console.error(`✗ ${r.name}: ${r.diffPct !== undefined ? `${r.diffPct.toFixed(3)}% diff` : r.reason}${r.diffPath ? ` → ${r.diffPath}` : ''}`);
  }
}

console.log(`\n${results.length} asset(s) checked: ${passed} passed, ${failed} failed${missing ? ` (${missing} missing baselines)` : ''}`);

if (missing) {
  console.error('\nRun with VREG_UPDATE=1 to create baselines from the current snapshots.');
}

if (failed) {
  process.exit(1);
}

console.log('Visual regression passed.');
